$(document).ready(function(){
	$('.back').click(function(){
		$('.login-form').fadeOut(100);
		$('.order-prod').fadeOut(100);
		$(this).fadeOut();
		$('.edit-comp').fadeOut(0);
		$('.add-prod').fadeOut(0);
		$('.add-offer').fadeOut(0);
	});
	//$('body').hide();
	$('.messages').find('.cont').niceScroll({
		cursorwidth:10,
		cursorborderradius:0
	});
	$('.messages').find('.msgs').niceScroll({
		cursorwidth:10,
		cursorborderradius:0
	});
	$('.fa-times').click(function(){
		$('.back').fadeIn();
		$('#del').fadeIn();
	});
	$('.fa-pencil-square-o').click(function(){
		$('.back').fadeIn();
		$('#cp-edit-form').fadeIn();
	});
	$('.back').click(function(){
		$('#cp-edit-form').fadeOut();
		$('#del').fadeOut();
	});
	$('.notfs').niceScroll({
		cursorwidth:10,
		cursorborderradius:0
	});
	$('.my-msgs').niceScroll({
		cursorwidth:10,
		cursorborderradius:0
	});
	$('.contents').click(function(){
		$('.msg').find('.mark').fadeOut();
		$('.notfs').fadeOut();
		$('.my-msgs').fadeOut();
	});
	$('#fa-bullhorn').click(function(){
		$('.msg').find('.mark').fadeOut();
		$('.my-msgs').fadeOut();
		$(this).find('.mark').fadeToggle();
		$('.notfs').fadeToggle();
	});
	$('#fa-envelope-o').click(function(){
		$('.notfs').fadeOut();
		$('.msg').find('.mark').fadeOut();
		$(this).find('.mark').fadeToggle();
		$('.my-msgs').fadeToggle();
	});

});